// log -> registro
console.log("Hello");

// error -> exibe mensagens de erro
console.error(new Error("Deu erro!"));

// table -> exibe informações tabuladas de forma mais organizada
let lista = ["ayo", "what da dog doin", 55];
console.table(lista);

// time & timeEnd -> exibe o tempo necessário para realização de uma operação.
console.time();
console.timeEnd();

// trace -> exibe a stacktrace de todos os pontos do código
console.trace();